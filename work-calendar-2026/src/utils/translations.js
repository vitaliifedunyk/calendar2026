export const formatCurrency = (amount, locale = "en") => {
  const locales = {
    en: "en-US",
    uk: "uk-UA",
    it: "it-IT",
  };

  return new Intl.NumberFormat(locales[locale] || "en-US", {
    style: "currency",
    currency: "EUR",
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  }).format(amount);
};
